
# For loop - whenever total numbers is reached, loop breaks and does not keep printing

for i in range(5):            #breaks the loop after 10 since numbering starts from "0". start defaults to 0, and stop is omitted! 
    print(i)


for i in range(1, 5):            #Return an object that produces a sequence of integers from start (1 inclusive) to stop (11 exclusive) by step.
    print(i)


for i in range(1, 9, 2):            #1 is start, 11 is stop, 2 is step (adds 2 to every preceeding number)
    print(i)

